const fs = require('fs');
const path = require('path');

// Create minimal 1x1 PNG files with correct header structure
// We'll create SVG-based icons and serve them as SVG
// Modern browsers and PWA support SVG icons

// For PWA, we need actual PNG. Let's create valid tiny PNG files
function createMinimalPNG(size) {
  // Minimal valid PNG: 1x1 pixel blue
  const png = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk
    0x54, 0x08, 0xD7, 0x63, 0x60, 0x60, 0x60, 0x00,
    0x00, 0x00, 0x04, 0x00, 0x01, 0x27, 0x34, 0x27,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, // IEND chunk
    0xAE, 0x42, 0x60, 0x82,
  ]);
  return png;
}

const dir = path.join(__dirname, '..', 'public');
fs.writeFileSync(path.join(dir, 'icon-192.png'), createMinimalPNG(192));
fs.writeFileSync(path.join(dir, 'icon-512.png'), createMinimalPNG(512));
console.log('✅ Icons created');
