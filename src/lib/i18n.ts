/** Detect browser's preferred language and map to supported codes */
let browserLang = 'en';
if (typeof navigator !== 'undefined') {
  const navLang = navigator.language?.split('-')[0] || 'en';
  const supported = ['yue', 'zh', 'en', 'vi', 'ja', 'ko'];
  browserLang = supported.includes(navLang) ? navLang : 'en';
  // Map 'zh' browser to 'yue' by default (Hong Kong user), but let user override
  if (navLang === 'zh') browserLang = 'yue';
}
export const getBrowserLang = () => browserLang;

/**
 * Get translated UI string for the given language.
 * Supports {0}, {1}, etc. for parameter substitution.
 */
export function t(lang: string, key: string, ...args: string[]): string {
  const bundle = bundles[lang];
  if (!bundle) {
    // Fallback: try English first, then yue
    const fallback = bundles.en[key] || bundles.yue[key] || key;
    return args.reduce((s, arg, i) => s.replace(`{${i}}`, arg), fallback);
  }
  let text = bundle[key] || bundles.en[key] || bundles.yue[key] || key;
  args.forEach((arg, i) => {
    text = text.replace(`{${i}}`, arg);
  });
  return text;
}
